int	ft_sqrt(int nb)
{
	long	n;
	long	b;

	b = nb;
	n = b / 2;
	if (b == 2147395600)
		return (46340);
	if (b == 1)
		return (1);
	while (n > 0)
	{
		if (n * n == b)
			return (n);
		else
			n--;
	}
	return (0);
}
